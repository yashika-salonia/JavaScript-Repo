//While loop 
let i=1
while(i<10){
    console.log(`Value of i: ${i}`);
    i+=2;
}

//while loop on array
let myArr=['blue','black','grey']
let arr=0
while(arr<myArr.length){
    console.log(`Key: ${arr} , Value: ${myArr[arr]}`);
    arr++;
}

//do while  loop
let num=11
do{
    console.log(`Number: ${num}`);
    num+=3
} while(num<=10);